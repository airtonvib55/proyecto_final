
package com.emergentes.modelo;


public class AsientosR {
    private int id;
    private int filaP;
    private int columnaP;

    public AsientosR() {
        this.id = 0;
        this.filaP = 0;
        this.columnaP = 0;
    }

    public int getId() {
        return id;
    }

    public int getFilaP() {
        return filaP;
    }

    public int getColumnaP() {
        return columnaP;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setFilaP(int filaP) {
        this.filaP = filaP;
    }

    public void setColumnaP(int columnaP) {
        this.columnaP = columnaP;
    }
    
    
    
}
